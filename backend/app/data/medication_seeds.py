def _expand_generic(
    generic: str,
    category: str,
    rx_type: str,
    disease_tags: List[str],
    source: str,
    brand_variants: List[str] = None,
    spec_pool: List[str] = None,
    indications: str = "",
    usage: str = "",
    contra: str = "",
    adverse: str = "",
    notes: str = "",
    n_manufacturers: int = 8,
    n_specs: int = 2,
) -> List[Dict[str, Any]]:
    """按通用名 × N 厂商 × N 规格组合生成条目（真实国内药品库的常态）。"""
    out: List[Dict[str, Any]] = []
    brand_variants = brand_variants or [generic]
    spec_pool = spec_pool or _SPEC_POOL_TABLET
    # 在厂商池中循环挑选 n_manufacturers 家厂商，每家取 n_specs 个规格
    mf_pool = _MANUFACTURERS_POOL[: n_manufacturers]
    for i, mf in enumerate(mf_pool):
        for j in range(n_specs):
            spec = spec_pool[(i + j) % len(spec_pool)]
            brand = brand_variants[(i + j) % len(brand_variants)]
            # 商品名：如果与通用名相同，加厂商后缀，避免完全相同的 (name, spec) 重复
            name = brand if brand != generic else generic
            out.append(_build_seed(
                name=name,
                generic=generic,
                spec=spec,
                manufacturer=mf,
                category=category,
                rx_type=rx_type,
                disease_tags=disease_tags,
                source=source,
                indications=indications,
                usage=usage,
                contra=contra,
                adverse=adverse,
                notes=notes,
            ))
    return out


