# 四、体质雷达图维度计算
# ═══════════════════════════════════════════════════════════════════

# 9 体质的雷达图维度（用于可视化展示）
RADAR_DIMENSIONS = [
    "气虚", "阳虚", "阴虚", "痰湿",
    "湿热", "血瘀", "气郁", "特禀", "平和",
]


def compute_radar_scores(answers: List[Dict[str, Any]], primary_type: str) -> Dict[str, int]:
    """根据答题情况 + AI 得出的主体质，生成一份 9 维雷达图得分（0~100）。

    由于一期题库仅 8 题，这里采用"主体质满分 + 其他维度按答题情绪倾向加减"的
    轻量算法保证展示效果；二期题库升级后会改成标准 9 体质得分算法。
    """
    # 初始化所有维度到 30 分（基线）
    scores: Dict[str, int] = {k: 30 for k in RADAR_DIMENSIONS}

    # 将"主体质"映射到雷达维度
    primary_key = primary_type.replace("质", "") if primary_type else "平和"
    if primary_key in scores:
        scores[primary_key] = 85
    else:
        # 若主体质识别失败，默认加强平和维度
        scores["平和"] = 75

    # 根据答题倾向微调其他维度
    negative_keywords = ["总是", "经常", "很差", "较差"]
    neutral_keywords = ["偶尔", "一般"]
    for ans in answers or []:
        v = str(ans.get("answer_value", "")) if isinstance(ans, dict) else str(getattr(ans, "answer_value", ""))
        if any(k in v for k in negative_keywords):
            # 症状明显，除主体质外其他维度普遍 +5
            for k in scores:
                if k != primary_key and scores[k] < 75:
                    scores[k] += 3
        elif any(k in v for k in neutral_keywords):
            for k in scores:
                if k != primary_key and scores[k] < 70:
                    scores[k] += 1

    # 保证 0~100 范围
    for k in scores:
        scores[k] = max(0, min(100, scores[k]))

    return scores


